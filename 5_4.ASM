;例5.4，计算Y=5X+8
SSEG SEGMENT STACK
STK DB 20 DUP(0)
SSEG ENDS
DSEG SEGMENT
ARGX DB 15;X的值
RLTY DW 0 ;Y的值
DSEG ENDS
CSEG SEGMENT
	ASSUME CS:CSEG,DS:DSEG
	ASSUME SS:SSEG
START:	MOV AX,DSEG
	MOV DS,AX
	MOV AX,SSEG
	MOV SS,AX
	MOV SP,SIZE STK
	MOV AL,ARGX
	MOV BL,5
	MUL BL
	ADD AX,8
	MOV WORD PTR RLTY[0],AX
	MOV AX,4C00H
	INT 21H
CSEG ENDS
END START;程序结束
