;例5.3，已知内存中的两个十六进制数据，将二者压缩成为一个字节的数据
SSEG SEGMENT STACK
STK DB 20 DUP(0)
SSEG ENDS
DSEG SEGMENT
HEX DB 0BH,04H
DATA DB 0
DSEG ENDS
CSEG SEGMENT
	ASSUME CS:CSEG,DS:DSEG,SS:SSEG
START:	MOV AX,DSEG
	MOV DS,AX
	MOV AX,SSEG
	MOV SS,AX
	MOV SP,SIZE STK
	MOV AX,WORD PTR HEX[0]
	MOV CL,4
	SHL AH,CL;高位左移4
	OR  AX,0FF0H;将中间8位置1
	AND AL,AH
	MOV DATA,AL
;将合并之和的显示出来,显示ASCII码为4BH的字符‘K’
	MOV DL,AL
	MOV AH,02H
	INT 21H
	MOV AX,4C00H
	INT 21H
CSEG ENDS
	END START
